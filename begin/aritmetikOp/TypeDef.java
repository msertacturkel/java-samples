package aritmetikOp;

public class TypeDef {

	public static void main(String[] args) {
		byte b=127;
		short s=32000;
		long l=3000000000L;
		//An integer literal is of type long if it is suffixed with an ASCII letter L or l (ell);
		//otherwise it is of type int
		System.out.println(b+" "+s+" "+l);
	}

}
